export type CycleReportInput = {
  cycle: Cycle;
  synthesis: SynthesisRow;
  goals: Goal[];
  diff?: DiffRow | null;
  // interview_id → its coverage row (null = never checked). Optional section.
  coverageByInterview?: Record<string, CoverageRow | null>;
  interviews: InterviewRow[];
};

// Confidence value → localized label (falls back to the raw value).
function confLabel(c: string | null | undefined): string {
  const t = tr(STR);
  const map: Record<string, string> = {
    high: t.confHigh,
    medium: t.confMedium,
    low: t.confLow,
  };
  if (!c) return "—";
  return map[c.toLowerCase()] ?? c;
}

function confidenceBadge(confidence: string): string {
  const t = tr(STR);
  const c = confidence.toLowerCase();
  return `<span class="badge conf-${esc(c)}"><span class="dot"></span>${esc(
    t.confidence,
  )}: ${esc(confLabel(c))}</span>`;
}

function statusBadge(status: DiffStatus): string {
  const t = tr(STR);
  const label: Record<DiffStatus, string> = {
    new: t.statusNew,
    changed: t.statusChanged,
    dropped: t.statusDropped,
    unchanged: t.statusUnchanged,
  };
  return `<span class="badge st-${esc(status)}"><span class="dot"></span>${esc(
    label[status],
  )}</span>`;
}

function findingHtml(f: Finding, titleFor: (id: string) => string): string {
  const t = tr(STR);
  const evidence = f.evidence
    .map(
      (e) =>
        `<li class="quote">“${esc(e.quote)}”<span class="src">${esc(
          titleFor(e.interview_id),
        )}</span></li>`,
    )
    .join("");
  const rec = f.recommendation
    ? `<p class="rec"><strong>${esc(t.recommendation)}:</strong> ${esc(
        f.recommendation,
      )}</p>`
    : "";
  return `<div class="finding">
  <p class="statement"><span class="fid">${esc(f.id)}</span>${esc(f.statement)}</p>
  <p class="meta-row">${confidenceBadge(f.confidence)}<span class="count">${esc(
    t.interviewsCount(f.support_count),
  )}</span><span class="count">${esc(t.quotes(f.evidence.length))}</span></p>
  ${evidence ? `<ul class="quotes">${evidence}</ul>` : ""}
  ${rec}
</div>`;
}

function diffEntryHtml(e: DiffEntry): string {
  const t = tr(STR);
  const conf =
    e.confidence || e.prev_confidence
      ? `<span class="count">${esc(t.confidence)}: ${esc(
          confLabel(e.prev_confidence),
        )} → ${esc(confLabel(e.confidence))}</span>`
      : "";
  const why = e.why ? `<p class="why">${esc(e.why)}</p>` : "";
  return `<div class="diff-entry">
  <p class="statement">${esc(e.statement)}</p>
  <p class="meta-row">${statusBadge(e.status)}${conf}</p>
  ${why}
</div>`;
}

