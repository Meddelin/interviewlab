// Build the standalone HTML report for a cycle. Sections: title/date/wave meta,
// executive summary (the markdown artifact, converted), findings by goal (confidence
// badges + evidence), the wave-over-wave diff (when present), and per-interview coverage
// scores (when provided). Pure function of its inputs — safe to unit-test.
export function buildCycleReportHtml(input: CycleReportInput): string {
  const t = tr(STR);
  const { cycle, synthesis, goals, diff, coverageByInterview, interviews } =
    input;

  const titleFor = (id: string): string => {
    const found = interviews.find((i) => i.id === id);
    return found?.title ?? t.unknownInterview(id.slice(0, 8));
  };

  const dateStr = new Date(synthesis.created_at).toLocaleDateString(undefined, {
    year: "numeric",
    month: "long",
    day: "numeric",
  });

  // --- Executive summary: the (possibly user-edited) markdown artifact. -----------
  const summaryMd =
    synthesis.content_md.trim() || synthesis.doc.executive_summary || "";
  const summaryHtml = summaryMd
    ? `<section>
  <h2 class="section">${esc(t.executiveSummary)}</h2>
  <div class="prose">${miniMarkdownToHtml(summaryMd)}</div>
</section>`
    : "";

  // --- Findings by goal. -----------------------------------------------------------
  const docGoals = synthesis.doc.goals.length > 0 ? synthesis.doc.goals : goals;
  const byGoal = new Map<string, Finding[]>();
  for (const f of synthesis.doc.findings) {
    const arr = byGoal.get(f.goal_id) ?? [];
    arr.push(f);
    byGoal.set(f.goal_id, arr);
  }
  const goalsHtml = docGoals
    .map((g) => {
      const findings = byGoal.get(g.id) ?? [];
      const body =
        findings.length === 0
          ? `<p class="na">${esc(t.noFindingsForGoal)}</p>`
          : findings.map((f) => findingHtml(f, titleFor)).join("\n");
      return `<div class="goal">
  <div class="goal-head"><span class="gid">${esc(g.id)}</span><h3>${esc(
    g.text,
  )}</h3></div>
  ${body}
</div>`;
    })
    .join("\n");

  // --- Open questions (small, only when present). -----------------------------------
  const openQs = synthesis.doc.open_questions ?? [];
  const openQuestionsHtml =
    openQs.length > 0
      ? `<section>
  <h2 class="section">${esc(t.openQuestions)}</h2>
  <div class="prose"><ul>${openQs.map((q) => `<li>${esc(q)}</li>`).join("")}</ul></div>
</section>`
      : "";

  // --- Diff vs previous wave (optional). --------------------------------------------
  let diffHtml = "";
  if (diff && diff.doc.by_goal.length > 0) {
    const goalBlocks = diff.doc.goals
      .map((g) => {
        const entries =
          diff.doc.by_goal.find((x) => x.goal_id === g.id)?.entries ?? [];
        if (entries.length === 0) return "";
        return `<div class="goal">
  <div class="goal-head"><span class="gid">${esc(g.id)}</span><h3>${esc(
    g.text,
  )}</h3></div>
  ${entries.map(diffEntryHtml).join("\n")}
</div>`;
      })
      .filter(Boolean)
      .join("\n");
    diffHtml = `<section>
  <h2 class="section">${esc(t.diffTitle)}</h2>
  ${diff.doc.summary ? `<p class="summary-line">${esc(diff.doc.summary)}</p>` : ""}
  ${goalBlocks}
</section>`;
  }

  // --- Per-interview coverage scores (optional). --------------------------------------
  let coverageHtml = "";
  if (coverageByInterview && interviews.length > 0) {
    const rows = interviews
      .map((i) => {
        const cov = coverageByInterview[i.id];
        const cell = cov
          ? `<span class="score-num">${Math.round(cov.doc.score)}</span><span class="bar"><i style="width:${Math.max(
              0,
              Math.min(100, Math.round(cov.doc.score)),
            )}%"></i></span>`
          : `<span class="na">${esc(t.noCoverage)}</span>`;
        return `<tr><td>${esc(i.title)}</td><td class="score">${cell}</td></tr>`;
      })
      .join("\n");
    coverageHtml = `<section>
  <h2 class="section">${esc(t.coverageTitle)}</h2>
  <table class="coverage">
    <thead><tr><th></th><th>${esc(t.coverageScore)}</th></tr></thead>
    <tbody>${rows}</tbody>
  </table>
</section>`;
  }

  return `<!doctype html>
<html>
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>${esc(cycle.name)}</title>
<style>${REPORT_CSS}</style>
</head>
<body>
<div class="page">
<header class="report">
  <div class="eyebrow">${esc(t.researchWave)}</div>
  <h1>${esc(cycle.name)}</h1>
  <div class="meta">${esc(dateStr)} · ${esc(t.interviews(interviews.length))}</div>
</header>
${summaryHtml}
<section>
  <h2 class="section">${esc(t.findingsByGoal)}</h2>
  ${goalsHtml}
</section>
${diffHtml}
${coverageHtml}
${openQuestionsHtml}
<footer class="report">${esc(t.generatedBy(new Date().toLocaleDateString()))}</footer>
</div>
</body>
</html>`;
}

// Save an HTML string as a file via a Blob + transient anchor — the same mechanism the
// existing .md exports use (works in the Tauri webview and the browser preview alike).
export function downloadHtmlReport(html: string, filename: string): void {
  const blob = new Blob([html], { type: "text/html;charset=utf-8" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = filename;
  a.click();
  URL.revokeObjectURL(url);
}

// A filesystem-safe report filename derived from the cycle name.
export function reportFileName(cycleName: string): string {
  const slug = cycleName
    .trim()
    .replace(/[\\/:*?"<>|]+/g, "")
    .replace(/\s+/g, "-")
    .slice(0, 60);
  return `${slug || "cycle"}-report.html`;
}
